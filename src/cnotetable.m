function nnote = cnotetable(n)
switch n
    case 0
        nnote = -1;
    case 1
        nnote = -0.5;
    case 2
        nnote = 0;
    case 3
        nnote = 1;
    case 4
        nnote = 1.5;
    case 5
        nnote = 2;
    case 6
        nnote = 2.5;
    case 7
        nnote = 3;
    case 8
        nnote = 4;
    case 9
        nnote = 4.5;
    case 10
        nnote = 5;
    case 11
        nnote = 5.5;
end

end

